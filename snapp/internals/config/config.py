from .config_wrapper import wrapper


# ----------------------------------------------------------------
#                            Time Zone
# ----------------------------------------------------------------

class TimeZone:
    zone: str = ''


# ----------------------------------------------------------------
#                              Logger
# ----------------------------------------------------------------

class Logger:
    level: str = ''


# ----------------------------------------------------------------
#                              Locale
# ----------------------------------------------------------------

class Locale:
    default: str = ''


# ----------------------------------------------------------------
#                               DB
# ----------------------------------------------------------------

class Database:
    host: str = ''
    port: str = ''
    db: str = ''
    user: str = ''
    password: str = ''


# ----------------------------------------------------------------
#                              Redis
# ----------------------------------------------------------------

class Redis:
    address: str = ''
    ttl: str = 60
    key_prefix: str = ''


# ----------------------------------------------------------------
#                              Celery
# ----------------------------------------------------------------

class Celery:
    broker: str = ''


# ----------------------------------------------------------------
#                              Config
# ----------------------------------------------------------------

class Config:
    time_zone: TimeZone = TimeZone()
    logger: Logger = Logger()
    locale: Locale = Locale()
    database: Database = Database()

    def __init__(self):
        wrapper(self)

    redis: Redis = Redis()
    celery: Celery = Celery()


# ----------------------------------------------------------------
#                       public config variable
# ----------------------------------------------------------------

config: Config = Config()
